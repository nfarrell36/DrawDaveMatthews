def draw_extended_diamond(radius, color, level):
    turtle.pencolor(color)
    turtle.fillcolor(color)
    if level == 0:
        turtle.left(186)
    else:
        turtle.begin_fill()
        turtle.forward(radius)
        turtle.left(93)
        turtle.forward((radius) * (math.sin(math.radians(6)) / math.sin(math.radians(87))))
        turtle.left(93)
        turtle.forward(radius)
        turtle.end_fill()
        draw_extended_diamond(radius,color, level-1)


